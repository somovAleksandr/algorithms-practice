"""
Модуль для нахождения минимального значения из трёх чисел с помощью лямбды.
"""

# Лямбда-выражение для нахождения минимума из трёх чисел
min_of_three = lambda a, b, c: a if a < b and a < c else (b if b < c else c)

if __name__ == "__main__":
    # Пример использования
    result = min_of_three(9, 8, 5)
    print(f"Минимальное значение: {result}")