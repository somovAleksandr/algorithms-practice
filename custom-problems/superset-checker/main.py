def check_superset(set1, set2):
    """
    Проверяет отношение между двумя множествами.
    Возвращает строку с результатом.
    """
    if set1 == set2:
        return "Множества равны."
    elif set1 > set2:  # строгое супермножество
        return "Объект является чистым супермножеством."
    else:
        return "Супермножество не обнаружено."

# Для запуска и вывода в консоль
if __name__ == "__main__":
    print(check_superset({1, 8, 3, 5}, {3, 5}))
    print(check_superset({90, 100}, {3, 5}))
    print(check_superset({1, 3, 5, 8}, {1, 3, 5, 8}))