def calculate_circumference(radius):
    """
    –í—ã—á–∏—Å–ª—è–µ—Ç –¥–ª–∏–Ω—É –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏ –ø–æ —Ñ–æ—Ä–º—É–ª–µ C = 2œÄr.

    Args:
        radius (float): —Ä–∞–¥–∏—É—Å –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏

    Returns:
        float: –¥–ª–∏–Ω–∞ –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏, –æ–∫—Ä—É–≥–ª—ë–Ω–Ω–∞—è –¥–æ 2 –∑–Ω–∞–∫–æ–≤ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
    """
    return round(2 * pi * radius, 2)


