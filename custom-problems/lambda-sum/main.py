"""
Модуль для вычисления суммы трёх чисел с использованием вложенных лямбда-выражений.

Функция sum3 создает замыкание, которое позволяет последовательно передавать три аргумента:
    sum3(a)(b)(c) = a + b + c

Пример:
    >>> sum3(2)(4)(6)
    12
"""

def sum3(a):
    """
    Возвращает функцию, ожидающую второй аргумент.
    
    Args:
        a (int, float): Первое число.
    
    Returns:
        function: Лямбда-функция, ожидающая второй аргумент.
    """
    return lambda b: lambda c: a + b + c


# Пример использования
if __name__ == "__main__":
    result = sum3(2)(4)(6)
    print(f"sum3(2)(4)(6) = {result}")