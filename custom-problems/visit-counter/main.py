def calc_qty_visit(city_name):
    """
    Возвращает функцию-счётчик, которая при каждом вызове увеличивает 
    количество "посещений" указанного города и выводит текущее значение.

    Аргументы:
        city_name (str): Название города, который "посещаем".

    Возвращает:
        function: Внутренняя функция без аргументов, при вызове:
            - увеличивает счётчик на 1,
            - печатает название города и текущее количество посещений.

    Пример:
        >>> moscow = calc_qty_visit("Москва")
        >>> moscow()
        Москва 1
        >>> moscow()
        Москва 2
    """
    count = 0

    def inner():
        nonlocal count
        count += 1
        print(city_name, count)

    return inner


