def cube(n):
    """
    Возвращает куб заданного числа.

    Аргумент:
        n (int или float): Число, для которого нужно вычислить куб.

    Возвращает:
        int или float: Результат возведения числа в третью степень (n³).

    Пример:
        >>> cube(3)
        27
        >>> cube(-2)
        -8
    """
    return n ** 3


def print_cubes(start, end):
    """
    Выводит кубы чисел в заданном диапазоне.

    Аргументы:
        start (int): Начало диапазона (включительно).
        end (int): Конец диапазона (включительно).

    Пример вывода:
        1 в кубе = 1
        2 в кубе = 8
        ...
    """
    print(f"Кубы чисел от {start} до {end}:\n")
    for i in range(start, end + 1):
        print(f"{i} в кубе = {cube(i)}")


# --- Основная программа ---
if __name__ == "__main__":
    print_cubes(1, 10)